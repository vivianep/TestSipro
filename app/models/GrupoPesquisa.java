package models;

public class GrupoPesquisa {
	
	private long id;
	private String nome;
	private long ano_criacao;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public long getAno_criacao() {
		return ano_criacao;
	}
	public void setAno_criacao(long ano_criacao) {
		this.ano_criacao = ano_criacao;
	}
	
	
	
	
}
