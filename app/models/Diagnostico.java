package models;

public class Diagnostico {

	private String nome;
	private boolean genetico;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public boolean isGenetico() {
		return genetico;
	}
	public void setGenetico(boolean genetico) {
		this.genetico = genetico;
	}
	
}
